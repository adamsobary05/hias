                        <p><h2>5.Jenis Ikan Koi</h2></p>

                           <p><h4>1.Ikan Koi Kohaku</h4></p>
                         <img src="img/blog-img/a.jpg" width="20%" >
                         <p>Pada dasarnya Kohaku memang dikenal sebagai jenis ikan koi yang cukup mahal. Meskipun demikian, hal ini tidak menyurutkan niat para pecinta ikan hias untuk memiliki jenis koi yang satu ini di rumah. Sebagai gambaran, harga ikan koi kecil dari jenis Kohaku berada pada kisaran (Rp 100 ribu per ekor). Untuk yang sudah dewasa, harga bisa melambung hingga (Rp 2 juta per ekor).</p>
                   
                          <p><h4>2.Ikan Koi Slayer</h4></p>
                          <img src="img/blog-img/b.jpg" width="20%" >
                         <p>Ikan koi dari jenis Slayer platinum pernah dihargai (Rp 5 juta per ekor). Namun harga juga cukup bervariasi tergantung ukuran ikan tersebut. Sedangkan harga ikan koi butterfly masih berada pada kisaran ratusan ribu sehingga cocok untuk Anda yang memiliki budget minim.</p>
                   
                           <p><h4>3.Ikan Koi Chagoi</h4></p>
                          <img src="img/blog-img/c.jpg" width="20%" >
                          <p>Ikan koi Chagoi merupakan variasi koi yang juga masuk dalam daftar koi langka. Ikan ini mempunyai ciri yang khas yaitu warna perunggu atau coklat dengan pola berbentuk jaring pada bagian punggung (ukuran 5cm harga Rp.150.000)</p>
                   
                           <p><h4>4.Ikan Koi Tancho</h4></p>
                          <img src="img/blog-img/d.jpg" width="20%" >
                          <p>Berbicara mengenai harga, Tancho juga termasuk kelompok ikan koi berharga mahal. Harga ikan koi anakan dari jenis Tancho berada pada kisaran (Rp 125 ribu per ekor). Untuk koi Tancho dewasa, harga bisa mencapai (Rp 500 ribuan per ekor). Berikut ini daftar lengkap harga koi Tancho berdasarkan ukurannya.</p>      
                   
                             <p><h4>5.Ikan Koi Jepang</h4></p>
                          <img src="img/blog-img/e.jpg" width="20%" >
                          <p>Untuk Anda yang tertarik dengan ikan koi asli Jepang, berikut ini daftar harga yang perlu Anda ketahui. Nama yang dicantumkan belum mencakup semua jenis ikan koi Jepang karena jumlahnya yang terlalu banyak (Rp.200.000).</p>
